<h1>Principle</h1>
<p>The Minidome or Portable Light Dome is an automated 3D digitizing solution, developed by the computer vision group <a href="http://www.esat.kuleuven.be/psi/visics"><b><span style="color:#ffbb88">VISICS</b></span></a> at KULeuven.
</p>

<p>
It consists of a small, light-weight dome of light
sources and a digital camera. It can carry both SLR cameras as machine vision cameras.
The approach is geared towards an easy-to-use, relatively inexpensive solution
for virtually inspection of objects, where both the 2D aspect ( color, intensity, or other representations
through filtering and relighting) as well as the 2D+/3D aspect ( by estimating the 3D surface characteristics
are being considered. The <a href="specs.php"><span style="color:yellow">results</span></a> allow for photorealistic virtual re-lighting and non-photorealistic 
rendering of the objects in real-time through the use of programmable graphics hardware. </p>

<p>For portability, the device is built out of 4 identical parts, and is quickly deployable on-site. It
allows a wide range of object sizes, with an easy calibration procedure one can switch easily between both macro as well as wide-angle digitization </p>
