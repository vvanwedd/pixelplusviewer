<br><p>
The Minidome provides a flexible, cost effective alternative to the traditional scanning approach. 
And whereas the traditional techniques focus mainly on the 3D surface properties, a surface is not only characterized by it's 3D nature, but equally by it's color, reflectance properties, albedo, BRDF,.. The minidome is designed to deliver results on both sides, while at the same time optimizing ease of use and flexibility.
</p>
<p>The current portable dome has a radius of 60~cm and is a discrete illumination hemisphere consisting of 260
white power LEDs positioned both on the knots and in the center of the edges. The LEDs are 5W Lambertian
emitters (over a 120\degree angle), resulting in homogeneous illumination. 
Each LED can be individually lit and the corresponding images can be
recorded by the camera mounted on top. The entire recording procedure is programmable and fully
automatic.</p>
